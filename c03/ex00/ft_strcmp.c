int		ft_strcmp(char *s1, char *s2)
{
	int count;
	int somad;

	somad = 0;
	count = 0;
	while (*s1 == *s2 && (*s1 != '\0' && *s2 != '\0'))
	{
		s1++;
		s2++;
	}
	somad = *s1 - *s2;
	return (somad);
}
